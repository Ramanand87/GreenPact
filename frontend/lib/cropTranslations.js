// Function to get translated crop name
export const getTranslatedCropName = (cropName, language = 'en') => {
  if (!cropName) return cropName;
  
  // Normalize the crop name (trim, capitalize first letter of each word)
  const normalizedName = cropName
    .trim()
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join(' ');
  
  const translation = cropTranslations[normalizedName];
  
  if (translation && translation[language]) {
    return translation[language];
  }
  
  // If no translation found, return original name
  return cropName;
};

